package com.cts.project.model;

//This is the token class to receive the generated token
public class Token {
	
	private String token;

	public Token() {
		super();
	}

	public Token(String token) {
		super();
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
	

}
